package com.ycit.bean.modal;

/**
 * 资讯类
 *
 * @author xlch
 * @Date 2018-03-30 10:12
 */
public class News {

    private int id;
    private String img;
    private String url;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
